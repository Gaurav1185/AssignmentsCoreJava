public class Qno5_DifferenceAndProduct {
    public static void main(String[] args) {
        int i=45;
        int k=32;
        int sum;
        int difference;
        int product;
        sum=i + k;
        System.out.println("The Addition Of Given Number is "+sum);
        difference =i - k;
        System.out.println("The Difference between these two number is "+difference);
        product =i * k;
        System.out.println("The product of these two numbers is "+product);
    }
}
